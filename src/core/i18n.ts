export type TKey = keyof typeof en;

const dicts: Record<Language, typeof en> = { en, de, pt };

let current: Language = 'en';

export function setLanguage(lang: Language): void {
  current = lang;
}

export function getLanguage(): Language {
  return current;
}

/** Übersetzung der aktiven Sprache (Fallback: Englisch). */
export function t(key: TKey): string {
  return dicts[current][key] ?? en[key];
}

/** Übersetzung mit {platzhaltern}. */
export function tf(key: TKey, vars: Record<string, string | number>): string {
  return t(key).replace(/\{(\w+)\}/g, (_, name: string) =>
    vars[name] !== undefined ? String(vars[name]) : `{${name}}`,
  );
}
